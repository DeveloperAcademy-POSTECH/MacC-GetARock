import UIKit

extension UIView {
    func setUIOfBandIntroduceViewAndRepertoireCellView() {
        self.backgroundColor = UIColor.backgroundBlue
        self.layer.cornerRadius = 15
        self.layer.borderWidth = 1
        self.layer.borderColor = UIColor.dividerBlue.cgColor
    }
}
